#As tuplas, diferente de outras linguagens, permitem dados de tipos diferentes dentro da mesma tupla.
pessoa = ('Gustavo', 39, 'M', 99.88)
print(pessoa)
#A única modificação que podemos fazer nas tuplas, é DELETA-LA com o comando "del".
# (Esse comando serve para qualquer variável)
del(pessoa)
print(pessoa)